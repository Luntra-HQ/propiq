const fs = require('fs');
const path = require('path');

const testOutputFile = process.argv[2];
const reportFile = process.argv[3];
const logDir = process.argv[4] || path.join(path.dirname(testOutputFile), 'console-logs');

// Check if test output exists
if (!fs.existsSync(testOutputFile)) {
  console.error(`Test output file not found: ${testOutputFile}`);
  process.exit(1);
}

// Read test results
let testResults;
try {
  const content = fs.readFileSync(testOutputFile, 'utf8');
  testResults = JSON.parse(content);
} catch (error) {
  console.error('Error parsing test results:', error.message);
  testResults = { suites: [] };
}

// Read console logs
const allErrors = [];
const allWarnings = [];

if (fs.existsSync(logDir)) {
  const logFiles = fs.readdirSync(logDir).filter(f => f.endsWith('.json'));

  logFiles.forEach(file => {
    try {
      const logData = JSON.parse(fs.readFileSync(path.join(logDir, file), 'utf8'));
      if (logData.errors) allErrors.push(...logData.errors);
      if (logData.warnings) allWarnings.push(...logData.warnings);
    } catch (error) {
      console.error(`Error reading log file ${file}:`, error.message);
    }
  });
}

// Generate markdown report
let report = `# 🐛 PropIQ Automated Bug Report

**Generated:** ${new Date().toISOString()}
**Test Suite:** Automated Bug Detection
**Test Output:** ${path.basename(testOutputFile)}

---

## 📊 Summary

`;

// Count test results
const allSpecs = testResults.suites?.flatMap(s => s.specs || []) || [];
const failedTests = allSpecs.filter(s => {
  const results = s.tests?.[0]?.results || [];
  return results.some(r => r.status === 'failed');
});
const passedTests = allSpecs.filter(s => {
  const results = s.tests?.[0]?.results || [];
  return results.every(r => r.status === 'passed');
});

report += `- ✅ Passed: ${passedTests.length}\n`;
report += `- ❌ Failed: ${failedTests.length}\n`;
report += `- 🚨 Console Errors: ${allErrors.length}\n`;
report += `- ⚠️  Console Warnings: ${allWarnings.length}\n\n`;

if (failedTests.length === 0 && allErrors.length === 0) {
  report += `## 🎉 All Clear!\n\nNo bugs detected in this test run.\n`;
} else {
  report += `## 🚨 CRITICAL ISSUES DETECTED\n\n`;
}

// Failed tests
if (failedTests.length > 0) {
  report += `### ❌ Failed Tests\n\n`;
  failedTests.forEach((spec, i) => {
    const test = spec.tests?.[0];
    const result = test?.results?.[0];

    report += `#### ${i + 1}. ${spec.title}\n\n`;
    report += `**Status:** FAILED\n\n`;

    if (result?.error) {
      report += `**Error:**\n\`\`\`\n${result.error.message || 'Unknown error'}\n\`\`\`\n\n`;
      if (result.error.stack) {
        report += `**Stack Trace:**\n\`\`\`\n${result.error.stack}\n\`\`\`\n\n`;
      }
    }
  });
}

// Console errors
if (allErrors.length > 0) {
  report += `### 🚨 Console Errors\n\n`;
  allErrors.slice(0, 10).forEach((err, i) => {
    report += `#### Error ${i + 1}/${allErrors.length}\n\n`;
    report += `**Time:** ${err.timestamp}\n\n`;
    report += `**Type:** ${err.type}\n\n`;
    report += `**Message:** ${err.message}\n\n`;
    if (err.location) report += `**Location:** ${err.location}\n\n`;
    report += `---\n\n`;
  });

  if (allErrors.length > 10) {
    report += `_... and ${allErrors.length - 10} more errors. See full logs for details._\n\n`;
  }
}

// Console warnings
if (allWarnings.length > 0) {
  report += `### ⚠️  Console Warnings (Top 5)\n\n`;
  allWarnings.slice(0, 5).forEach((warn, i) => {
    report += `${i + 1}. **${warn.message}** (${warn.timestamp})\n`;
  });
  report += `\n`;
}

report += `---\n\n`;
report += `## 📂 Log Files\n\n`;
report += `Console logs saved to: \`${logDir}\`\n\n`;

if (fs.existsSync(logDir)) {
  const logFiles = fs.readdirSync(logDir).filter(f => f.endsWith('.json'));
  if (logFiles.length > 0) {
    report += `**Files:**\n`;
    logFiles.forEach(file => {
      report += `- ${file}\n`;
    });
  }
}

report += `\n---\n\n`;
report += `*Generated by PropIQ Automated Bug Detection System*\n`;

// Write report
try {
  const reportDir = path.dirname(reportFile);
  if (!fs.existsSync(reportDir)) {
    fs.mkdirSync(reportDir, { recursive: true });
  }
  fs.writeFileSync(reportFile, report);
  console.log(`\n✅ Bug report generated: ${reportFile}\n`);
} catch (error) {
  console.error('Error writing report:', error.message);
  process.exit(1);
}
